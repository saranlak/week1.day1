package Week1.Day1;

public class PositiveOrNegative {
	
	public static void main(String[] args) {
		int check_val=0;
		
		if(check_val>0) 
		{
			System.out.println("Given number : "+check_val+" is positive");
		}
		else if(check_val<0) 
		{
			System.out.println("Given number : "+check_val+" is negative");			
		}
		else 
		{
			System.out.println("Given number : "+check_val+" is neither negative nor positive");
		}
		}

}
