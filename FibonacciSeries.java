package Week1.Day1;

public class FibonacciSeries {

	public static void main(String[] args) {
		
		int var1 = 6, var2 = 0, var3 = 1;
		System.out.println("fibonacci range "+ var1);
		
		for(int i=1;i<=var1;++i)
		{
			System.out.println(var2+ ", ");
		int var4=var2+var3;
		var2=var3;
		var3=var4;
	
		}
		
	}
		
}
